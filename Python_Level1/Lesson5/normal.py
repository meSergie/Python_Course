do = {'1': ef.change_dir,
      '2': ef.list_dir,
      '3': ef.delete_dir,
      '4': ef.make_dir,
      'm': menu
    }
while True:
    key = input('Выберите действие:')
    if key:
        if do.get(key):
            if key in ['1','3','4']:
                do[key]()
            else:
                do[key]()
        elif key == 'q':
            sys.exit()
        else:
            print('Такого действия в меню нет')

