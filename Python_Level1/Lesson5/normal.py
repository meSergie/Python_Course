import os, sys
import easy_functions as ef

def menu():
    print('1. Перейти в папку\n2. Просмотреть содержимое текущей папки\n3. Удалить папку\n4. Создать папку')
    print('Press \'q\' to exit, \'m\' to return menu')
menu()
# def get_dirname():
